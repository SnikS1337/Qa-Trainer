function choice(
  q: string,
  correct: string,
  wrongOne: string,
  wrongTwo: string,
  wrongThree: string,
  exp: string
): QuestionChoice {
  return {
    type: 'choice',
    q,
    opts: [correct, wrongOne, wrongTwo, wrongThree],
    ans: 0,
    exp,
  };
}

